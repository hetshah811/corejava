import java.util.Scanner;

public class j33forloop {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        int n;
        System.out.println("Enter number :");
        n=sc.nextInt();
        for(int i=1;i<=n;i++){
            System.out.println(i);
        }
    
        
    }
}